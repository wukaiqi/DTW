function [output] = gauss_filter (x)
%sigma=0.5时
%gau=[0.0090,0.4910,0.4910,0.0090]
%sigma=1 时
%gau=[0.0009 0.0175 0.1295 0.3521 0.3521 0.1295 0.0175 0.0009]
%sigma=2时
gau=[ 0.0002    0.0010    0.0045    0.0159    0.0431    0.0913    0.1506   ...
    0.1933    0.1933    0.1506    0.0913 0.0431    0.0159    0.0045    0.0010    0.0002];
%gau为高斯函数的一维模板，可以由这个函数得到:fspecial('gaussian', [1 n], sigma)。

x = conv(x,gau);
output=x(1:length(x)-length(gau)+1);

end